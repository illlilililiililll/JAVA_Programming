// PI에 10000을 곱하면 0.92가 남는데, 이를 정확하게 표현하지 못함.
// PI * 10 * 10 * 100과 PI * 10000의 결과가 다른데, 순서대대로 곱했을 때 소수 부분의 처리 과정에 차이가 있는 것으로 보인다.
// PI * 10 * 10 * 100은 31415.92, PI*10000은 31415.920000000002이다.

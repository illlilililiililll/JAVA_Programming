// 원기둥의 부피 공식 : PI*r*r*h 이용

public class HW3 {
    public static void main(String[] args) {
        final double PI = 3.141592;
        int r = 10;
        int h = 100;

        System.out.printf("원기둥의 반지름 : %d\n", r);
        System.out.printf("원기둥의 높이 : %d\n", h);
        System.out.println("원기둥의 부피는 : " + PI*r*r*h);
        // System.out.println(10*10*100*PI);
    }
}

